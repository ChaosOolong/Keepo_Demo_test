// Colors.h
#ifndef COLORS_H
#define COLORS_H

// 16位RGB颜色定义 (RGB565格式)
#define COLOR_BG           0xf7be    // 淡粉色 RGB(255, 235, 238)
#define COLOR_BTN          0x97D6    // 淡绿色 RGB(144, 238, 144)
#define COLOR_BTN_TEXT     0x0000     // 黑色
#define COLOR_TITLE        0x3186    // 深粉色 RGB(200, 100, 150)
#define COLOR_BORDER       0x7BEF    // 灰色 RGB(128, 128, 128)
#define COLOR_HIGHLIGHT    0xAEDC    // 浅黄色高亮
#define COLOR_WARNING      0xFD20    // 橙色警告
#define COLOR_ERROR        RED       // 红色错误
#define COLOR_TOUCH_FEEDBACK 0xFD20  // 触摸反馈橙色
#define COLOR_BTN_BLACK    0x9cf3
#define COLOR_URGENCY      0xfb4d
#define COLOR_MIDDLE       0xff49

#endif